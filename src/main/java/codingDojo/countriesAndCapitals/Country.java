package codingDojo.countriesAndCapitals;

public class Country {
	
	public Country(String name, String capital) {
		super();
		this.name = name;
		this.capital = capital;
	}
	
	public String getName() {
		return name;
	}
	
	public String getCapital() {
		return capital;
	}
	
	private String name;
	private String capital;

}
